#include "checker.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return (s1[i] - s2[i]);
}

void	free_splitted(char **splitted)
{
	int	i;

	i = 0;
	while (splitted[i])
	{
		free(splitted[i]);
		i++;
	}
	free(splitted);
}

void	handle_action_error(t_stacks *s, char **splitted_actions)
{
	free_splitted(splitted_actions);
	free_and_return_null(&(s->stack_a), &(s->stack_b), NULL, NULL);
	error_exit_function(EXIT_FAILURE);
}
